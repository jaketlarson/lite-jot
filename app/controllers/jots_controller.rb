class JotsController < ApplicationController

  def create
    folder_autogenerated = false
    topic_autogenerated = false

    # check if typing a jot for a shared folder
    # this is to prevent users from typing a jot to auto-create
    # a topic in a folder that is shared with them
    folder_is_owned = true
    folder_empty = false
    if !params[:folder_id].nil? && params[:folder_id].to_i > 0
      folder_check = Folder.find(params[:folder_id])
      if folder_check.user_id != current_user.id
        folder_is_owned = false
        folder_is_empty = folder_check.topics.count == 0 ? true : false
      end
    end

    if !folder_is_owned && folder_is_empty
      render :json => {:success => false, :error => "Jots cannot be created an empty folder shared with you."}, :status => :bad_request
      return
    end
    # end check

    if params[:folder_id].nil? || params[:folder_id].to_i <= 0 || Folder.where('id = ?', params[:folder_id]).nil?
      time = Time.new
      folder = current_user.folders.new
      folder.title = "New #{time.strftime('%b %d')}"
      folder.save
      folder_id = folder.id

      folder_autogenerated = true
    else
      folder_id = params[:folder_id]
    end

    if params[:topic_id].nil? || params[:topic_id].to_i <= 0 || Topic.where('id = ?', params[:topic_id]).nil?
      time = Time.new
      topic = current_user.topics.new
      topic.title = "New #{time.strftime('%b %d')}"
      topic.save
      topic_id = topic.id

      topic_autogenerated = true
    else
      topic_id = params[:topic_id]
    end

    folder = Folder.find(folder_id)
    folder.touch
    topic = Topic.find(topic_id)
    topic.folder_id = folder_id
    topic.save
    topic.touch # incase the update doesn't apply

    jot = current_user.jots.new(jot_params)
    jot.topic_id = topic_id

    if jot.save
      if topic_autogenerated && folder_autogenerated
        ser_jot = JotSerializer.new(jot, :root => false, :scope => current_user)
        ser_topic = TopicSerializer.new(topic, :root => false, :scope => current_user)
        ser_folder = FolderSerializer.new(folder, :root => false, :scope => current_user)

        render :json => {:success => true, :jot => ser_jot, :auto_folder => ser_folder, :auto_topic => ser_topic}
      
      elsif !topic_autogenerated && folder_autogenerated
        ser_jot = JotSerializer.new(jot, :root => false, :scope => current_user)
        ser_folder = FolderSerializer.new(folder, :root => false, :scope => current_user)

        render :json => {:success => true, :jot => ser_jot, :auto_folder => ser_folder}
      
      elsif topic_autogenerated && !folder_autogenerated
        ser_jot = JotSerializer.new(jot, :root => false, :scope => current_user)
        ser_topic = TopicSerializer.new(topic, :root => false, :scope => current_user)

        render :json => {:success => true, :jot => ser_jot, :auto_topic => ser_topic}
      
      else
        ser_jot = JotSerializer.new(jot, :root => false, :scope => current_user)
        render :json => {:success => true, :jot => ser_jot}
      end
    else
      render :json => {:success => false}, :status => :bad_request
    end
  end

  def update
    jot = current_user.jots.find(params[:id])

    if jot.content != params[:content] && !params[:content].nil?
      topic = current_user.topics.find(jot.topic_id)
      topic.touch
      folder = current_user.folders.find(topic.folder_id)
      folder.touch
    end

    if jot.user_id == current_user.id
      if jot.update(jot_params)
        ser_jot = JotSerializer.new(jot, :root => false, :scope => current_user)
        render :json => {:success => true, :jot => ser_jot}

      else
        render :json => {:success => false}, :status => :bad_request
      end
    else
      render :json => {:success => false, :error => "You do not have permission to modify this jot."}, :status => :bad_request
    end
  end

  def destroy
    jot = Jot.find(params[:id])

    if jot.user_id == current_user.id
      if jot.destroy
        render :json => {:success => true, :message => "Jot moved to trash."}
      else
        render :json => {:success => false, :error => "Could not delete jot."}, :status => :bad_request
      end
    else
      render :json => {:success => false, :error => "You do not have permission to delete this jot."}, :status => :bad_request
    end

  end

  protected

    def jot_params
      params.permit(:id, :content, :topic_id, :folder_id, :is_flagged)
    end
end
